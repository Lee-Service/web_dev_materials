                    <p>Daft Punk’s Homework is, in its pure existence, a study in contradictions. The debut album from Thomas Bangalter and Guy-Manuel de Homem-Christo arrived in 1997, right around the proliferation of big-beat and electronica—a twin-headed
                        hydra of dance music fads embraced by the music industry following the commercialization of early ’90s rave culture—but when it came to presumptive contemporaries from those pseudo-movements, Homework shared Sam Goody rack
                        space and not much else. Daft Punk’s introduction to the greater world also came at a time when French electronic music was gaining international recognition, from sturdy discotheque designs to jazzy, downtempo excursions—music
                        that sounded miles away from Homework’s rude, brutalist house music.
                    </p>

                    <p>In the 21 years since Homework’s release, Daft Punk have strayed far from its sound with globe-traversing electronic pop that, even while incorporating other elements of dance music subgenres, has more often than not kept house
                        music’s building blocks at arms’ length. 2001’s Discovery was effectively electronic pop-as-Crayola box, with loads of chunky color and front-and-center vocals that carried massive mainstream appeal. Human After All from 2005
                        favored dirty guitars and repetitive, Teutonic sloganeering, while the pair took a nostalgia trip through the history of electronic pop itself for 2013’s Random Access Memories. Were it not for a few choice Homework tracks
                        that pop up on 2007’s exhilarating live document Alive 2007, one might assume that Homework has been lost in the narrative that’s formed since its release—that of Daft Punk as robot-helmeted superstar avatars, rather than as
                        irreverent house savants.</p>

                    <p>But even as the straightforward and strident club fare on Homework remains singular within Daft Punk’s catalog, the record also set the stage for the duo’s career to this very day—a massively successful and still-going ascent to
                        pop iconography, built on the magic trick-esque ability to twist the shapes of dance music’s past to resemble something seemingly futuristic. Whether you’re talking about Bangalter and Homem-Christo’s predilection for global-kitsch
                        nostalgia, their canny and self-possessed sense of business savvy, or their willingness to wear their influences on their sleeve like ironed-on jean-jacket patches—it all began with Homework.
                    </p>

                    <p>It couldn’t possibly make more sense that a pair of musicians whose most recent album sounds like a theme park ride through pop and electronic music’s past got their big break at Disneyland. It was 1993, and schoolboy friends Bangalter
                        and Homem-Christo’s rock band with future Phoenix guitarist Laurent Brancowitz, Darlin’—named after a track from the 1967 Beach Boys album Wild Honey that the three shared an affinity for—had disbanded after a year of existence
                        that included a few songs released on Stereolab’s Duophonic label. (Melody Maker writer Dave Jennings notoriously referred to their songs as possessing “a daft punky thrash,” which led to the pair assuming the Daft Punk moniker.)</p>

                    <p>While attending a rave in Paris, Bangalter and Homem-Christo had a chance encounter with Glasgow DJ/producer Stuart McMillan, the co-founder of the Soma Recordings dance label; like any aspiring musicians would, they gave him a
                        demo tape of early Daft Punk music. The following year Soma released Daft Punk’s debut single “The New Wave,” a booming and acid-tinged instrumental that would later evolve into Homework cut “Alive.”
                    </p>

                    <p>A follow-up, “Da Funk” b/w “Rollin’ & Scratchin’,” hit shops in 1995; according to a Muzik profile two years later, its initial 2,000-platter pressing was “virtually ignored” until rave-electronica bridge-gap veterans the Chemical
                        Brothers started airing out its A-side during DJ sets. A major-label bidding war ensued, with Virgin as the victor which re-released “Da Funk” as a proper single in 1996 with non-Homework track “Musique” as its B-side. During
                        this time, Bangalter and Homem-Christo casually worked on the 16 tunes that would make up Homework in the former’s bedroom, utilizing what The Guardian’s Ben Osborne referred to in 2001 as “low technology equipment”—two sequencers,
                        a smattering of samplers, synths, drum machines, and effects, with an IOMEGA zip drive rounding out their setup.</p>

                    <p>Bangalter and Homem-Christo’s work ethic while assembling the bulk of Homework was of the type that makes sloths appear highly efficient by comparison: no more than eight hours a week, over the course of five months. “We have not
                        spent much time on Homework,” Bangalter casually bragged to POP. “The main thing is that it sounds good… We have no need to make music every day.” The songs were crafted with the intention of being released as singles (“We
                        do not really want to make albums,” Bangalter claimed in the same interview), Homework’s eventual sequencing a literal afterthought after the pair realized they had enough material to evenly fill four sides of two vinyl platters.
                        “Balance,” the pair said in unison when asked about Homework’s format-specific sequencing in Dance Music Authority following the album’s release. “It is done for balance.”
                    </p>

                    <p>Indeed, Homework is practically built to be consumed in side-long chunks; taking the album in at a single 75-minute listen can feel like running a 5K right after eating an entire pizza. Its A-side kicks off with the patient build
                        of “Daftendirekt”—itself a live-recording excerpt of introductory music used during a Daft Punk set at 1995’s I Love Techno festival in Ghent—and concludes with the euphoric uplift of “Phoenix”; the B-side opens with the literal
                        oceanic washes of “Fresh” before stretching its legs with the loopy, Gershon Kingsley-interpolating “Around the World” and the screeching fist-pump anthem “Rollin’ & Scratchin’.” The third side keeps things light with the flashy,
                        instructional “Teachers” before getting truly twisted on “Rock’n Roll,” and the fourth side takes a few rubbery detours before landing on the full-bodied “Alive”—the thicker and meaner final form of “The New Wave”—and, quixotically,
                        a slight and rewound “Da Funk” return, aptly titled “Funk Ad.”</p>

                    <p> Bangalter explained to POP that the title of Homework carries a few meanings: “You always do homework in the bedroom,” he stated, referencing the album’s homespun origins before elaborating on the didactic exercise that creating
                        the album represented: “We see it as a training for our upcoming discs. We would as well have been able to call it Lesson or Learning.” That instructional nature is reflexive when it comes to listeners’ presumptive relationship
                        with the album, as Homework practically represents a how-to for understanding and listening to house music.
                    </p>

                    <p>Nearly every track opens with a single sonic element—more often than not, that steady 4/4 rhythm inextricably tied to house music—adding every successive element of the track patiently, like a played-in-reverse YouTube video showcasing
                        someone taking apart a gadget to see what’s inside. Such a pedagogic approach can have its pitfalls; there’s always a risk of coming across as too rigid, and Daft Punk arguably fell victim to such dull, fussy didacticism later
                        in their careers. But they sidestep such follies on Homework by way of the purely pleasurable music they carefully assembled, piece-by-piece, for whoever was listening.
                    </p>

                    <p>Under the umbrella of house music, Homework incorporates a variety of sounds snatched from various musical subgenres—G-funk’s pleasing whine, the cut-up vocal-sample style of proto-UK garage made popular by frequent Daft Punk collaborator
                        Todd Edwards, disco’s delicious synths and glittery sweep—to craft a true musical travelogue that also hinted at the widescreen sonic scope they’d take later in their careers. Above all, the album represents a love letter to
                        black American pop music that’s reverberated through Daft Punk’s career to date—from Janet Jackson’s sample of “Daftendirekt” on her 2008 Discipline track “So Much Betta” to Will.i.am’s failed attempt to remix “Around the World”
                        the year previous, as well as the duo’s continued collaborations with artists ranging from Pharrell to Kanye West and the Weeknd.</p>

                    <p>The spirit of house music’s Midwestern originators is also literally and musically invoked throughout. Over the winding house-party groove of “Teachers,” Daft Punk pay homage to their formative influences, ranging from George Clinton
                        and Dr. Dre to Black house and techno pioneers like Lil Louis, DJ Slugo, and Parris Mitchell—and in a meta twist, the song’s structure itself is a literal homage to Mitchell’s 1995 Dance Mania! single “Ghetto Shout Out,” an
                        interpolation clearly telegraphed in the middle of Daft Punk’s astounding contribution to BBC’s Essential Mix series in 1997.</p>

                    <p>Alongside Daft Punk’s preoccupations with American popular music, Homework also carries a very specific and politically pointed evocation of their native Paris in “Revolution 909,” the fourth and final single released from Homework
                        that doubled as a critique of anti-rave measures taken by the French government after Jacques Chirac assumed power in 1995. “I don’t think it’s the music they’re after—it’s the parties,” Homem-Christo told Dance Music Authority,
                        with Bangalter adding, “They pretend [the issue is] drugs, but I don’t think it’s the only thing. There’s drugs everywhere, but they probably wouldn’t have a problem if the same thing was going on at a rock concert, because
                        that’s what they understand. They don’t understand this music which is really violent and repetitive, which is house; they consider it dumb and stupid.”
                    </p>

                    <p>“Revolution 909” opens with ambient club noise, followed by the intrusion of police sirens and intimidating megaphone’d orders to “stop the music and go home.” The accompanying Roman Coppola-helmed music video was even more explicit
                        in depicting the frequent clash between ravers and law enforcement that marked dance music’s rise to the mainstream in the early-to-mid-’90s; amidst a kitschy instructional video on making tomato sauce, a pair of cops attempt
                        to disperse a rave, a young woman escaping one of their grasps after he becomes distracted by a tomato sauce stain on his own lapel.</p>

                    <p>It’s been rumored, but never quite confirmed, that Bangalter himself appears in the video for “Revolution 909”—a slice of speculation gesturing towards the fact that Daft Punk’s Homework era was the time in which the duo began
                        embracing anonymity. The now-iconic robot helmets wouldn’t be conceived of until the Discovery era, and the magazine stories that came during Daft Punk’s pre-Homework days were typically accompanied by a fresh-faced photo of
                        the pair; during Homework’s promotional cycle, however, they donned a variety of masks to obscure their visages, including frog and pig-themed disguises.</p>

                    <p>In conversation with Simon Reynolds for The New York Times in 2013, the pair cited Brian De Palma’s glam-rock masterpiece Phantom of the Paradise as artistic inspiration for their decision to retain visual anonymity, and Daft Punk’s
                        press-shy tendencies (since Homework, the interviews they’ve chosen to take part in have been few and far between) are firmly situated in a long tradition of letting the music do the talking in dance culture—from the sci-fi
                        evasiveness of Drexciya and Aphex Twin’s relative reclusiveness to the preferred reticence of Burial and his contemporaries in the UK bass scene.</p>

                    <p>But refusing to turn themselves into rock stars upon Homework’s release also afforded Daft Punk a crucial element that has undoubtedly aided their perpetual ascent to the present-day: control. Retaining a sense of anonymity was
                        but one of the conditions that the pair struck with Virgin upon signing to the label before Homework’s release; while the music they released under the label (before signing to Columbia in 2013) was licensed exclusively to
                        Virgin, they owned it through their own Daft Trax production and management company.
                    </p>

                    <p>But Homework proved influential in other, more explicitly musical ways. G-house, an emergent dance subgenre in the mid-2010s dominated by acts like French duo Amine Edge & Dance, borrows liberally from Daft Punk’s own musical mash
                        of hip-hop’s tough sounds and house music’s pounding appeal; the dirty bloghouse bruisers of Parisian collective Ed Banger—founded by Pedro Winter aka Busy P, who acted as the group’s manager until 2008—would literally not
                        exist were it not for Homework, and that goes double for the party-hardy bloghouse micro-movement of the mid-late 2000s, which Ed Banger’s artists practically dominated. Parisian duo Justice, in particular, owe practically
                        the entirety of their 2007 landmark † to the scraping tension of “Rollin’ & Scratchin.”
                    </p>

                    <p>It’s tempting, too, to tie a connective thread between Homework and the brash sounds that proliferated during the peak heyday of the financial descriptor-cum-music genre known as EDM; close your eyes while listening to “Alive”’s
                        big-tent sweep and try not to imagine the tune destroying a festival crowd. But for all of Homework’s aggressive charms, it’s also retained a homespun intimacy in comparison to how positively widescreen Daft Punk’s music became
                        afterwards. “We focus on the illusion because giving away how it’s done instantly shuts down the sense of excitement and innocence,” Bangalter told Pitchfork in 2013, and the fact that two Beach Boys fans fiddling around in
                        their bedroom could conceive of something so generously in-your-face and playful as Homework might still stand as Daft Punk’s greatest illusion yet. Back to home.</p>
